package com.ceitechs.dproz.usermanagement.domain;

/**
 * Defines different contact methods
 * 
 * @author pdigumarthi
 *
 */
public enum ContactMethod {
	
	/**
	 * Only use the phone for text
	 */
	TEXT,
	
	/**
	 * Only use the phone for call
	 */
	CALL,
	
	/**
	 * Use the phone for call or text
	 */
	TEXTORCALL

}
